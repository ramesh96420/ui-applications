//Ternary Operation
console.log("Ternary Operation");
/* Ternary Operation
	1>2 condition is true than output is true.
	1>2 condition is false than output is false.
	? is seperated two parts such as
	1. condition  - this is ? right side
	2. output	   - this is ? left side


*/
console.log(1>2 ? true:false);

